# Operadores Aritméticos
# Suma
suma = a + b
# Resta
resta = a - b
# Multiplicación
multiplicacion = a * b
# División
division = a / b
# División entera
division_entera = a // b
# Módulo
modulo = a % b
# Exponenciación
exponenciacion = a ** b

print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("División entera:", division_entera)
print("Módulo:", modulo)
print("Exponenciación:", exponenciacion)

# Operadores de Comparación
# Igualdad
igual = (a == b)
# Desigualdad
desigual = (a != b)
# Mayor que
mayor_que = (a > b)
# Menor que
menor_que = (a < b)
# Mayor o igual que
mayor_igual = (a >= b)
# Menor o igual que
menor_igual = (a <= b)

print("Igual:", igual)
print("Desigual:", desigual)
print("Mayor que:", mayor_que)
print("Menor que:", menor_que)
print("Mayor o igual que:", mayor_igual)
print("Menor o igual que:", menor_igual)

# Operadores Lógicos
print("A", a)
