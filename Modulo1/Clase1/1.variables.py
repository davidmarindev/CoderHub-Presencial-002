# Variable: Es un espacio en memoria que almacena un valor.

# Reglas para nombrar variables:
# 1. No puede empezar con un número.
# 2. No puede contener espacios.
# 3. No puede contener caracteres especiales (excepto _).
# 4. No puede ser una palabra reservada de Python.

# snake_case: Es un estilo de nombrar variables donde las palabras se separan con guiones bajos (_).

# Las constantes se escriben en mayúsculas y se separan con guiones bajos (_) por convencion.

name = "David"
user_last_name = "Marin"
user_age = 28
user_is_active = True
PI = 3.14159
none_value = None

print("Mi nombre es:", name, user_last_name)
print(type(name))  # Imprime el tipo de dato de la variable name
print(type(user_age))
print(type(user_is_active))
print(type(PI))
print(type(none_value))

