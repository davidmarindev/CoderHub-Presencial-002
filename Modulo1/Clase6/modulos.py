# Modulos y librerias en python

# Un módulo es un archivo que contiene código Python. 
# Puede contener definiciones de funciones, clases y variables, y 
# también declaraciones y comandos ejecutables.

# Librería es un conjunto de módulos que se pueden utilizar en un programa.

# Existen 3 tipos de módulos en Python:

# 1. Módulos predefinidos: Son módulos que vienen incluidos en Python.
# 2. Módulos personalizados: Son módulos que creamos nosotros mismos.
# 3. Módulos de terceros: Son módulos que no vienen incluidos en Python y debemos instalar.

# Principales modulos predefinidos en Python

# math: Este módulo proporciona acceso a las funciones matemáticas definidas 
# por el estándar C.

# random: Este módulo implementa generadores de números pseudoaleatorios.

# os: Este módulo proporciona una forma portátil de utilizar la funcionalidad

# sys: Este módulo proporciona acceso a algunas variables utilizadas o 
# mantenidas por el intérprete y a funciones que interactúan fuertemente con el intérprete.

# datetime: Este módulo proporciona clases para manipular fechas y horas.

# json: Este módulo implementa un codificador y decodificador para el formato 
# de intercambio de datos JSON.

# string: Este módulo contiene algunas constantes, clases y métodos aplicables a 
# cadenas de texto.

# Modulos de Terceros

# Son módulos que no vienen incluidos en Python y debemos instalar.
# Algunos ejemplos son:

# requests: Este módulo permite enviar solicitudes HTTP de forma sencilla.
# numpy: Este módulo proporciona soporte para arreglos y matrices multidimensionales,
# gtts: Este módulo permite convertir texto a voz utilizando Google Text-to-Speech.

# Como importar módulos en Python
# Para importar un módulo en Python, utilizamos la palabra clave import.
import math
import random

# Como importar modulos de terceros en Python
# Para importar un módulo de terceros en Python, primero debemos instalarlo utilizando pip.
# Por ejemplo, para instalar el módulo requests, utilizamos el siguiente comando:

# pip install requests

# Una vez instalado, podemos importarlo en nuestro código de la siguiente manera:
import requests