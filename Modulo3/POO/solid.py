# SOLID: 

# Single Responsibility Principle
# Open/Closed Principle
# Liskov Substitution Principle
# Interface Segregation Principle
# Dependency Inversion Principle

# 1. Single Responsibility Principle (SRP)
# Cada clase debe tener una única responsabilidad o motivo para cambiar.

# 2. Open/Closed Principle (OCP)
# Las entidades de software (clases, módulos, funciones, etc.) deben estar 
# abiertas para la extensión, pero cerradas para la modificación.

# 3. Liskov Substitution Principle (LSP)
# Los objetos de una clase derivada deben poder sustituir a los objetos de la
# clase base sin alterar el comportamiento del programa.

# 4. Interface Segregation Principle (ISP)
# Los clientes no deben verse obligados a depender de interfaces que no utilizan.

# 5. Dependency Inversion Principle (DIP)
# Las clases de alto nivel no deben depender de las clases de bajo nivel; ambas deben depender de abstracciones.
# Las abstracciones no deben depender de los detalles; los detalles deben depender de las abstracciones